ZH: dict[str, str] = {**EN, **ZH_OVERRIDES}


def detect_system_lang() -> str:
    locale_name = QLocale.system().name().lower()
    return "zh" if locale_name.startswith("zh") else "en"


def set_lang(lang: str) -> None:
    global _current_lang
    normalized = (lang or "").strip().lower()
    if normalized not in SUPPORTED_LANGUAGES:
        normalized = detect_system_lang()
    _current_lang = normalized


def get_lang() -> str:
    return _current_lang


def t(key: str, **kwargs) -> str:
    primary = ZH if _current_lang == "zh" else EN
    secondary = EN if _current_lang == "zh" else ZH
    template = primary.get(key) or secondary.get(key) or key
    if not kwargs:
        return template
    try:
        return template.format(**kwargs)
    except Exception:
        return template


def available_languages() -> list[tuple[str, str]]:
    return [
        ("", t("lang.auto")),
        ("zh", t("lang.zh")),
        ("en", t("lang.en")),
    ]


